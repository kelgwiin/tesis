        <h4 class="modal-title text-center" id="myModalLabel"><i class="fa fa-info-circle"></i> Ayuda</h4>
      </div>
      <div class="modal-body">

      <span class="label label-primary">Responsabilidades</span>. Debe indicar los detalles de las responsabilidades de las distintas partes involucradas en el Servicio y su acordada 
      responsabilidades, incluido el Proveedor de Servicios y el Cliente.<br>

       <div class="alert alert-info" role="alert">
      <i><b>Ejemplo:</b></i><br>
      Responsabilidades del Proveedor:<br>
      El proveedor deberá proporcionar y mantener el sistema de TI utilizado por el Cliente. <br>
      El contrato de soporte de TI entre el proveedor y el cliente incluye todos los detalles de estas responsabilidades. <br>
      Además, el proveedor deberá: <br>
      • Asegurarse de que el software, servicios y equipos pertinentes están a disposición del cliente. <br>
      • Responder a las solicitudes de soporte dentro de los plazos indicados. <br>
      • Tomar medidas para escalar y resolver problemas en una forma oportuna y apropiada.<br> 
      • Mantener una buena comunicación con el cliente en todo momento. <br>
      Responsabilidades del Cliente:<br>
      El cliente utilizará el sistema de TI proporcionado por los proveedores según lo previsto. <br>
      El contrato de soporte de TI entre el proveedor y el cliente incluye todos los detalles del sistema de TI y sus usos previstos. <br>
      Además, el cliente deberá: <br>
      • Notificar problemas de manera oportuna. <br>
      • Proporcionar al Proveedor acceso a equipos, software y servicios para los fines de mantenimiento, actualizaciones y la prevención de fallos. <br>
      • Mantener una buena comunicación con el proveedor en todo momento.<br>

      </div>

